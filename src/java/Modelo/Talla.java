
package Modelo;

public class Talla {
    private int ID;
    private String numeroCalzado;

    public Talla() {
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getNumeroCalzado() {
        return numeroCalzado;
    }

    public void setNumeroCalzado(String numeroCalzado) {
        this.numeroCalzado = numeroCalzado;
    }
  
}
